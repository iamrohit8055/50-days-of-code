// for loop

let fruits = ["apple", "banana", "mango"];

for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}

// for ... of loop

let marks = [6,7,9,3,6,1,9,5,0,];
for(let item of marks){
    console.log(item);
}

// forEach usage   (This is not a loop, it is an array function)

fruits.forEach((i) =>{
    console.log(i);

})



